# อัญมณีจะหายไปในไม่ช้า คุณจะต้องการความช่วยเหลือ!

# findItems() ส่งคืนอาร์เรย์ของไอเทม
items = hero.findItems()

# รับอัญมณีแรกจากอาร์เรย์
# อย่าลืมว่าดัชนีแรกเป็น 0
gem0 = items[0]

# บอก Bruno ให้รับ gem0
hero.say("Bruno " + gem0)

# คุณสามารถอ้างอิงอัญมณีได้โดยไม่มีตัวแปร
hero.say("Matilda " + items[1])

# สร้างตัวแปรสำหรับอัญมณีสุดท้าย รายการ[2]:
gem2 = items[2]
# ไปยังตำแหน่งของอัญมณีนั้นโดยใช้ moveXY()
hero.moveXY(gem2.pos.x, gem2.pos.y)