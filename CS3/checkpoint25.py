# บูลีนเป็นค่าที่เป็น True หรือ False
# สัญลักษณ์ == หมายถึง "เท่ากับ?" ใช่ไหม
# ดังนั้น การเขียน A == B จึงถามคำถามว่า "A เท่ากับ B หรือไม่"
# คำตอบคือค่าบูลีน!
# คลิกปุ่ม "Hints" หากสับสน!

# คำถาม: 2 == 3 
# พูดคำตอบที่ถูกต้อง:
hero.say(False)

# คำถาม: 3 == 3
# ตอบคำถามถูกหรือผิด 2:
hero.say(True)

# คำถาม: "Three" == 3
# ตอบคำถามถูกหรือผิด 3:
hero.say(False)

# คำถาม: "Three" == "Three"
# ตอบคำถามถูกหรือผิด 4:
hero.say(True)

# คำถาม: 1 + 2 == 3
# ตอบคำถามถูกหรือผิด 5:
hero.say(True)