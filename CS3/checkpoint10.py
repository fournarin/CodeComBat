# คุณสมบัติ pos เป็นอ็อบเจ็กต์ที่มีคุณสมบัติ x และ y
# pos.x คือตัวเลขที่แสดงตำแหน่งแนวนอนบนแผนที่
# pos.y คือตัวเลขที่แสดงตำแหน่งแนวตั้งบนแผนที่

while True:
    enemy = hero.findNearestEnemy()
    if enemy:
        x = enemy.pos.x
        # เอาตำแหน่ง y ของศัตรู!
        y = enemy.pos.y # ∆ เปลี่ยนสิ่งนี้ เดี๋ยวนี้
        
        # บอกว่าตำแหน่ง x และ y คั่นด้วยเครื่องหมายจุลภาค
        hero.say(x + "," + y)
    else:
        hero.say("หยุด" + " " + "ยิง!")