# โค้ดคำสั่ง if-statement จะทำงานก็ต่อเมื่อเงื่อนไขของ if เป็น true (จริง) เท่านั้น
# ในเงื่อนไข == หมายถึง "เท่ากับ"
if 2 + 2 == 4:
    hero.say("เฮ้!")
if 2 + 2 == 5:
    hero.say("ใช่ คุณนั่นแหละ!")

