# โชคดีที่คำตอบทั้งหมดคือ "2"
def sayTwo(event):
    # ใช้ pet.say() เพื่อตอบ "2"
    pet.say("2")
    pass

# ใช้ pet.on() เพื่อจัดการกับอีเวนท์"hear" ด้วย sayTwo
pet.on("hear", sayTwo)
