# คุณถูกติดกับดัก!
# ส่งสัตว์เลี้ยงของคุณไปเอายาเพื่อพลังชีวิต!

def goFetch():
    # คุณสามารถใช้ลูปในฟังก์ชันตัวจัดการ
    while True:
        potion = hero.findNearestItem()
        if potion:
            # ใช้ pet.fetch เพื่อคาบยา
            pet.fetch(potion)
            pass

# เมื่อสัตว์เลี้ยงของคุณถูกเรียกออกมา มันจะทำให้เกิดอีเวนท์ "spawn"
# สิ่งนี้จะบอกให้สัตว์เลี้ยงของคุณเรียกใช้ฟังก์ชัน goFetch เมื่อเริ่มต้นระดับ
pet.on("spawn", goFetch)
